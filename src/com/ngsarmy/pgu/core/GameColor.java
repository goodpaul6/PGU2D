package com.ngsarmy.pgu.core;

/* GameColor class:
 * this is a data storage class with no methods, it just stores
 * public r, g, and b integers
 */
public class GameColor 
{
	public int r;
	public int g;
	public int b;
	
	public GameColor(int r, int g, int b)
	{
		this.r = r;
		this.g = g;
		this.b = b;
	}
}
